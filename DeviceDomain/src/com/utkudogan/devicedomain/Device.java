package com.utkudogan.devicedomain;

/**
 *
 * @author utku.dogan
 */
public class Device {

    String type;
    int id;

    public Device(String type, int id) {
        this.type = type;
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }



}
